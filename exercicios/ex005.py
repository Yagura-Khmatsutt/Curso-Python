'''
Faça um programa que leia um número Inteiro e mostre na tela o seu sucessor e seu antecessor.
'''
print('Olá')

num = float(input('Dígite um número para ver seu sucessor e antecessor: '))
antecessor = num - 1
sucessor = num + 1
print(f'O número {num} tem como antecessor {antecessor} e {sucessor} como sucessor')
print('FIM')
